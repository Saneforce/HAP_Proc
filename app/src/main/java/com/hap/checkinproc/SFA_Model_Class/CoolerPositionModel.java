package com.hap.checkinproc.SFA_Model_Class;

public class CoolerPositionModel {
    String id, name;

    public CoolerPositionModel(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
